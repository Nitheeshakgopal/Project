public class FactorialMethod1{
	public static void main(String[]args){
	factorial(7);	
	}
	public static void factorial(int num){
	    int fact=1;
		int i=1;
	    while(i<=num){
		fact=fact*i;
			       i++;	
        }
			System.out.println("factorial is "+ fact);
	       
		}
	        
	    
	
		}
		
	